bool MapObject::check_move(const int _x, const int _y, int object_x, int object_y) {

	int next_x = object_x + _x;
	int next_y = object_y + _y;

	if (next_x < 0 || next_x > 90 || next_y < 0 || next_y > 50) return false;
	if (_x) {
		if (checkXY(next_x, this->y)) return true;
		return false;
	}
	else if (_y) {
		if (checkXY(this->x, next_y)) return true;
		return false;
	}
}

bool MapObject::move(Direction d) {

	int next_x = 0, next_y = 0;
	switch (d) {
	case Direction::UP:
		next_x = 0, next_y = -1;
		break;
	case Direction::DOWN:
		next_x = 0, next_y = 1;
		break;
	case Direction::RIGHT:
		next_x = 1, next_y = 0;
		break;
	case Direction::LEFT:
		next_x = -1, next_y = 0;
		break;
	}

	if (check_move(next_x, next_y, this->x, this->y)) {

		this->x += next_x;
		this->y += next_y;

		return true;
	}

	else return false;

}



