void Trap::locateFootprint(ScenePtr scene, int footNum) {
	if (footNum == 1) {
		int _x = foot1->setX(foot1->X());
		int _y = foot1->setY(foot1->Y());
		foot1->locate(scene, _x, _y);
	}
	else {
		int _x = foot2->setX(foot2->X());
		int _y = foot2->setY(foot2->Y());
		foot2->locate(scene, _x, _y);
	}
}

void Trap::turn_trap() {

	switch (direction) {
	case Direction::UP:
		direction = Direction::DOWN;
		foot1->setImage(footImage(direction, true));
		foot2->setImage(footImage(direction, false));
		break;
	case Direction::DOWN:
		direction = Direction::UP;
		foot1->setImage(footImage(direction, true));
		foot2->setImage(footImage(direction, false));
		break;
	case Direction::RIGHT:
		direction = Direction::LEFT;
		foot1->setImage(footImage(direction, true));
		foot2->setImage(footImage(direction, false));
		break;
	case Direction::LEFT:
		direction = Direction::RIGHT;
		foot1->setImage(footImage(direction, true));
		foot2->setImage(footImage(direction, false));
		break;
	}
}

